    private void generateSubjectNo() {
        try {
            ResultSet subNo = MySQL.executeSearch("SELECT COUNT(`sub_no`) AS subno FROM `subject`");
            if (subNo.next()) {
                int usedNo = subNo.getInt("subno");
                int newNo = usedNo + 1;
                regSubjectNoField.setText(String.valueOf(newNo));
            } else {
                regSubjectNoField.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

