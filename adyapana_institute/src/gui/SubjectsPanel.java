    private void loadSubjectData(String subId) {
        if (!subId.isBlank()) {
            try {
                ResultSet subjectData = MySQL.executeSearch("SELECT * FROM `subject` WHERE `sub_no`='" + subId + "'");
                DefaultTableModel subTable = (DefaultTableModel) subjectDataTable.getModel();
                subTable.setRowCount(0);

                while (subjectData.next()) {
                    Vector<String> vector = new Vector<>();
                    vector.add(subjectData.getString("sub_no"));
                    vector.add(subjectData.getString("name"));
                    vector.add(subjectData.getString("price"));

                    subTable.addRow(vector);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                ResultSet subjectData = MySQL.executeSearch("SELECT * FROM `subject`");
                DefaultTableModel subTable = (DefaultTableModel) subjectDataTable.getModel();
                subTable.setRowCount(0);

                while (subjectData.next()) {
                    Vector<String> vector = new Vector<>();
                    vector.add(subjectData.getString("sub_no"));
                    vector.add(subjectData.getString("name"));
                    vector.add(subjectData.getString("price"));

                    subTable.addRow(vector);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

