    private void subjectDataTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_subjectDataTableMouseClicked
        if (evt.getClickCount() == 2) {
            int rowIndex = subjectDataTable.getSelectedRow();
            TableModel subjectTable = subjectDataTable.getModel();
            String subNo = String.valueOf(subjectTable.getValueAt(rowIndex, 0));

            if (subNo != null && !subNo.isBlank()) {
                updateButton.setEnabled(true);
                deleteButton.setEnabled(true);
                manageSearchButton.setEnabled(false);
                manageSubNoField.setEditable(false);
                manageSubNameField.setEditable(false);
                managePriceField.setEditable(true);

                try {
                    ResultSet result = MySQL.executeSearch("SELECT * FROM `subject` WHERE `sub_no`='" + subNo + "'");
                    if (result.next()) {
                        manageSubNoField.setText(subNo);
                        manageSubNameField.setText(result.getString("name"));
                        managePriceField.setText(result.getString("price"));
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_subjectDataTableMouseClicked

