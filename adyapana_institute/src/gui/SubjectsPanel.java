    private void addSubjectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSubjectButtonActionPerformed
        String subNo = regSubjectNoField.getText();
        String subName = regSubjectNameField.getText();
        String price = regPriceField.getText();

        if (subName.isBlank()) {
            JOptionPane.showMessageDialog(this, "Enter subject name", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Enter price", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                MySQL.executeIUD("INSERT INTO `subject`(`sub_no`,`name`,`price`) VALUES('" + subNo + "','" + subName + "','" + price + "')");
                JOptionPane.showMessageDialog(this, "Subject Registered Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearAddFields();
                loadSubjectData("");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addSubjectButtonActionPerformed

